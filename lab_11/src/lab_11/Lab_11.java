package lab_11;
public class Lab_11 {
    public static void main(String args[]) {
        System.out.println("--Show Triangle--");
        for(int i = 1; i <= 10; i = i+2) {
            for(int j = i; j <= 10; j++) {
                System.out.print("% ");
            }
            System.out.println("\n");
        }
        System.out.println("-----------------");
    }
}